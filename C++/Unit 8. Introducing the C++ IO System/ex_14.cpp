#include <iostream>
#include <cstdlib>

using namespace std;

int main() {
    cout.width(20);
    cout.setf(ios::left);
    cout.precision(6);
    cout.fill('*');
    cout << 1000.5364;
    cout << "\n\n";
    
    system("pause");
    return 0;
}